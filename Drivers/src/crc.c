/**
  * @brief CRC8表生成函数
  * @param  ptr 要存储表的数组指针地址
  * @param  Polynomial CRC多项式不计最高位
  * @retval 无
  */
void CreateCRC8(uint8_t *pCRC, uint8_t Polynomial)
{ //上次CRC值与被校验的数据异或，再CRC校验，校验过程最高位0左移1位，最高位1左移1位并且异或CRC
    uint16_t i = 0;
    uint8_t j = 0;
    uint8_t crc = 0;

    for (i = 0; i < 256; i++)
    {
        crc = (uint8_t)i;
        for (j = 0; j < 8; j++)
        {
            if (crc & 0x80)
            {
                crc <<= 1;
                crc ^= Polynomial;
            }
            else
            {
                crc <<= 1;
            }
        }
        *pCRC = crc;
        pCRC++;
    }
}

/**
  * @brief CRC16表生成函数
  * @param  ptr 要存储表的数组指针地址
  * @param  Polynomial CRC多项式不计最高位
  * @retval 无
  */
void CreateCRC16(uint16_t *pCRC, uint16_t Polynomial)
{
    uint16_t i = 0;
    uint8_t j = 0;
    uint16_t crc = 0;

    for (i = 0; i < 256; i++)
    {
        crc = (uint16_t)i << 8;
        for (j = 0; j < 8; j++)
        {
            if (crc & 0x8000)
            {
                crc <<= 1;
                crc ^= Polynomial;
            }
            else
            {
                crc <<= 1;
            }
        }
        *pCRC = crc;
        pCRC++;
    }
}

/**
  * @brief 计算CRC校验结果
  * @param  ptr 要计算的数组指针地址
  * @param  len 要计算的数组数据长度
  * @retval crc 返回CRC值
  */
uint8_t GetCRC8(const uint8_t *ptr, uint16_t len)
{
    uint8_t i = 0;
    uint8_t crc = 0; //CRC8字节初始化

    for (i = 0; i < len; i++)
    {
        crc = CRC8_Array[crc ^ *ptr]; //上次CRC值与被校验的数据异或，再CRC校验
        ptr++;
    }
    return (crc);
}

/**
  * @brief 计算CRC校验结果
  * @param  ptr 要计算的数组指针地址
  * @param  len 要计算的数组数据长度
  * @retval crc 返回CRC值
  */
uint16_t GetCRC16(const uint8_t *ptr, uint16_t len)
{
    uint8_t i = 0;
    uint8_t da = 0;
    uint16_t crc = 0;

    for (i = 0; i < len; i++)
    {
        da = (uint8_t)(crc >> 8);      // 以8位二进制数的形式暂存CRC的高8位
        crc <<= 8;                     // 左移8位，相当于CRC的低8位乘以256
        crc ^= CRC16_Array[da ^ *ptr]; // 高8位和当前字节相加后再查表求CRC ，再加上以前的CRC
        ptr++;
    }
    return (crc);
}